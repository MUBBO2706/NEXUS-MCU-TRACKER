export function getSortedMcuTitles(mode: 'theatrical' | 'chronological'): McuTitle[] {
  const orderList = mode === 'theatrical' ? THEATRICAL_ORDER_IDS : CHRONOLOGICAL_ORDER_IDS;
  return [...MCU_TITLES].sort((a, b) => {
    const idxA = orderList.indexOf(a.id);
    const idxB = orderList.indexOf(b.id);
    return (idxA === -1 ? 999 : idxA) - (idxB === -1 ? 999 : idxB);
  });
}

